    public void sales()
    {
        String totalcost = txtTC.getText();
        String pay = txtPay.getText();
        String balance = txtB.getText();
        
        Connection();
        
        int lastid = 0;
        
        String query = ("insert into sales(totalcost,pay,balance)values(?,?,?)");
        try {
            
            pst = con.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, totalcost);
            pst.setString(2, pay);
            pst.setString(3, balance);
            pst.executeUpdate();
            rs = pst.getGeneratedKeys();
            
            if(rs.next())
            {
                lastid = rs.getInt(1);
            }
            int rows = jTable2.getRowCount();
            
            String query1 = ("Insert into sales_products(sales_id,iname,price,qty,total)values(?,?,?,?,?)");
            pst1 = con.prepareStatement(query1);
            
            String iname = "";
            String price ;
            String qty ;
            int tot = 0;
            
            for (int i=0;i<jTable2.getRowCount();i++)
            {
                iname = (String)jTable2.getValueAt(i, 0);
                price = (String)jTable2.getValueAt(i, 1);
                qty = (String)jTable2.getValueAt(i, 2);
                tot = (int)jTable2.getValueAt(i, 3);
                
                pst1.setInt(1, lastid);
                pst1.setString(2, iname);
                pst1.setString(3, price);
                pst1.setString(4, qty);
                pst1.setInt(5, tot);
                pst1.executeUpdate();
                
                
            }
            
            JOptionPane.showMessageDialog(this, "Sales completed....");
    
            
        } catch (SQLException ex) {
            Logger.getLogger(Bill.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
   
