    private void txtItemIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtItemIDKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()== KeyEvent.VK_ENTER)
        {
            try {
                String id = txtItemID.getText();
                pst = con.prepareStatement("select * from item where id = ?");
                pst.setString(1, id);
                rs = pst.executeQuery();

                if(rs.next()==false)
                {
                    JOptionPane.showMessageDialog(this, "Item id not found");
                }
                else
                {
                    String name = rs.getString("name");
                    txtItemN.setText(name.trim());
          
                    String price = rs.getString("price");
                    txtPrice.setText(price.trim());
        
                    txtQty.requestFocus();
                }
                
                
                
            } catch (SQLException ex) {
                Logger.getLogger(Bill.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_txtItemIDKeyPressed

